INSERT INTO study_resourcing(identifier, summ_4_rept_indicator, study_protocol_identifier, funding_mechanism_code,
                            nih_institute_code, nci_division_program_code, serial_number, active_indicator, 
                            date_last_created, date_last_updated)
SELECT nextval('hibernate_sequence'), false, sp.identifier, 'P30', 'CA', 'OD', p30.serialnumber, true, now(), now()
FROM study_protocol sp
JOIN study_site ss ON (ss.study_protocol_identifier = sp.identifier AND ss.functional_code = 'LEAD_ORGANIZATION')
JOIN research_organization ro ON (ss.research_organization_identifier = ro.identifier)
JOIN organization org ON (ro.organization_identifier = org.identifier)
JOIN organization_p30_grant p30 ON (p30.organization_id = CAST(org.assigned_identifier AS bigint))
JOIN document_workflow_status dws ON (sp.identifier = dws.study_protocol_identifier)
WHERE sp.status_code = 'ACTIVE'
  AND sp.nci_grant IS NULL
  AND (dws.study_protocol_identifier, dws.identifier) IN (SELECT study_protocol_identifier, MAX(identifier)
                                                          FROM document_workflow_status
                                                          GROUP BY study_protocol_identifier)
  AND dws.status_code IN ('ABSTRACTION_VERIFIED_RESPONSE', 'ABSTRACTION_VERIFIED_NORESPONSE')
  AND sp.identifier NOT IN (SELECT study_protocol_identifier 
                            FROM study_resourcing 
                            WHERE funding_mechanism_code = 'P30' 
                              AND summ_4_rept_indicator = false);

DROP TABLE organization_p30_grant;

UPDATE study_protocol sp
SET nci_grant = ( (SELECT MAX(nih_institute_code) 
                   FROM study_resourcing sr 
                   WHERE sp.identifier = sr.study_protocol_identifier 
                     AND nih_institute_code = 'CA')
                 IS NOT NULL )
WHERE status_code = 'ACTIVE'
  AND nci_grant IS NULL;